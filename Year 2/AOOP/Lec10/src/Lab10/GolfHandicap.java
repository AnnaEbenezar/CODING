    public String caculateHandicap() {
        double handicap = 0;
        double[] diffScore = new double[model.getRowCount()];
        for (int i = 0; i < diffScore.length; i++) {
            diffScore[i] = (Integer.parseInt((String) model.getValueAt(i, 3)) - Double.parseDouble((String) model.getValueAt(i, 1))) * 113 / Integer.parseInt((String) model.getValueAt(i, 2));
        }
        Arrays.sort(diffScore);
        double avgDiff = 0;
        switch (diffScore.length) {
            case 5:
                for (int i = 0; i < 1; i++) {
                    avgDiff += diffScore[i];
                }
                break;
            case 6:
                for (int i = 0; i < 1; i++) {
                    avgDiff += diffScore[i];
                }
                break;
            case 7:
                for (int i = 0; i < 2; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 2;
                break;
            case 8:
                for (int i = 0; i < 2; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 2;
                break;
            case 9:
                for (int i = 0; i < 3; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 3;
                break;
            case 10:
                for (int i = 0; i < 3; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 3;
                break;
            case 11:
                for (int i = 0; i < 4; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 4;
                break;
            case 12:
                for (int i = 0; i < 4; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 4;
                break;
            case 13:
                for (int i = 0; i < 5; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 5;
                break;
            case 14:
                for (int i = 0; i < 5; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 5;
                break;
            case 15:
                for (int i = 0; i < 6; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 6;
                break;
            case 16:
                for (int i = 0; i < 6; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 6;
                break;
            case 17:
                for (int i = 0; i < 7; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 7;
                break;
            case 18:
                for (int i = 0; i < 8; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 8;
                break;
            case 19:
                for (int i = 0; i < 9; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 9;
                break;
            case 20:
                for (int i = 0; i < 10; i++) {
                    avgDiff += diffScore[i];
                }
                avgDiff = avgDiff / 10;
        }
        handicap = (int) (0.96 * avgDiff * 100) / 100.0;
        return "" + handicap;
    }

